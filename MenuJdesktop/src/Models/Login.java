package Models;

public class Login {

	String cod_login;
	String nome_login;
	String usuario;
	String senha;
	public String getCod_login() {
		return cod_login;
	}
	public void setCod_login(String cod_login) {
		this.cod_login = cod_login;
	}
	public String getNome_login() {
		return nome_login;
	}
	public void setNome_login(String nome_login) {
		this.nome_login = nome_login;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	
	
	
	
}
